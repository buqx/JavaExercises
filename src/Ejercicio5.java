public class Ejercicio5 {
    public static void ejecutar() {
        System.out.println("=== EJERCICIO 5: Bucle for (Números pares) ===");

        System.out.println("Números pares del 1 al 50:");
        for (int i = 2; i <= 50; i += 2) {
            System.out.print(i + " ");
        }
        System.out.println();
    }
}